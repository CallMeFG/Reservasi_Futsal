    private void loadTabelInventaris() {
        DefaultTableModel model = (DefaultTableModel) tblInventaris.getModel();

        model.setRowCount(0);
        model.setColumnCount(0);
        
        model.addColumn("Inventaris ID");
        model.addColumn("Nama Barang");
        model.addColumn("Jumlah");

        FutsalController controller = new FutsalController();
        List<Inventaris> daftarInventaris = controller.getAllInventaris();

        for (Inventaris inventaris : daftarInventaris) {
            model.addRow(new Object[]{
                inventaris.getInventarisId(),
                inventaris.getNamaBrg(),
                inventaris.getJumlah()
            });
        }
    }

