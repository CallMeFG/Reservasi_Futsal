    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int row = tblInventaris.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Silakan pilih baris yang ingin diubah.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String id = tblInventaris.getValueAt(row, 0).toString();
        String nama = tblInventaris.getValueAt(row, 1).toString();
        String jumlah = tblInventaris.getValueAt(row, 2) != null ? tblInventaris.getValueAt(row, 2).toString() : "";

        // Buat objek User 
        Inventaris inventarisToEdit = new Inventaris(id, nama, jumlah); // Password dikosongkan

        FormInputInventaris form = new FormInputInventaris(this, true, inventarisToEdit);
        form.setVisible(true); 
        loadTabelInventaris();
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int row = tblInventaris.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Silakan pilih baris yang ingin dihapus terlebih dahulu.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus inventaris ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            String id = tblInventaris.getValueAt(row, 0).toString();
            FutsalController controller = new FutsalController();
            if (controller.hapusInventaris(id)) {
                JOptionPane.showMessageDialog(this, "Inventaris berhasil dihapus.");
                loadTabelInventaris();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus inventaris.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

